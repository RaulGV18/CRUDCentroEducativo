        private void cargatabla() throws SQLException{
        DefaultTableModel modelo = (DefaultTableModel)jtalumno.getModel();
        modelo.setNumRows(0);
        
        alumnoDaoImp al=alumnoDaoImp.getInstance();
        List<alumno> alumnos=al.getalumnosnotincurso(idunidad);
        String [] fila =new String[12];
        for (alumno alum:alumnos){
            fila[0]=""+alum.getId();
            fila[1]=""+alum.getDni();
            fila[2]=""+alum.getNombre();
            fila[3]=""+alum.getApellido1();
            fila[4]=""+alum.getApellido2();
            fila[5]=""+alum.getfNacimiento();
            fila[6]=""+alum.getTelefono();
            fila[7]=""+alum.getEmail();
            fila[8]=""+alum.getDireccion();
            fila[9]=""+alum.getCp();
            fila[10]=""+alum.getPoblacion();
            fila[11]=""+alum.getProvincia();
            modelo.addRow(fila);
        }
    }
        private void cargatabla2() throws SQLException{
        DefaultTableModel modelo = (DefaultTableModel)jtMatriculado.getModel();
        modelo.setNumRows(0);
        
        
        alumnoDaoImp al=alumnoDaoImp.getInstance();
        List<alumno> alumnos=al.getalumnoscurso(idunidad);
        String [] fila =new String[14];
        for (alumno alum:alumnos){
            fila[0]=""+alum.getId();
            fila[1]=""+alum.getDni();
            fila[2]=""+alum.getNombre();
            fila[3]=""+alum.getApellido1();
            fila[4]=""+alum.getApellido2();
            fila[5]=""+alum.getfNacimiento();
            fila[6]=""+alum.getTelefono();
            fila[7]=""+alum.getEmail();
            fila[8]=""+alum.getDireccion();
            fila[9]=""+alum.getCp();
            fila[10]=""+alum.getPoblacion();
            fila[11]=""+alum.getProvincia();
            matriculaDaoImp mat = matriculaDaoImp.getInstance();
            matricula matr=mat.getbyAlumnoycurso(alum.getId(), idunidad);
            fila[12]=""+matr.getfMatricula();
            fila[13]=""+matr.getDescripcion();
            modelo.addRow(fila);
        }
    }
        
        
    
