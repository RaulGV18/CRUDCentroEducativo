    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int fila= jtMatriculado.getSelectedRow();
        matriculaDaoImp matr = matriculaDaoImp.getInstance();
        matricula id=null;
        try {
            id = matr.getbyAlumnoycurso(Integer.parseInt(String.valueOf(jtMatriculado.getValueAt(fila,0))), idunidad);
        } catch (SQLException ex) {
            Logger.getLogger(frmMatriculas.class.getName()).log(Level.SEVERE, null, ex);
        }
        System.out.println(LocalDate.now());
        matricula mat= new matricula(id.getId(),Integer.parseInt(String.valueOf(jtMatriculado.getValueAt(fila,0))),idunidad,txtDescripcion.getText(),Date.valueOf(txtfMatricula.getText()),Date.valueOf(LocalDate.now()));
        try {
            matr.update(mat);
            cargatabla();
            cargatabla2();
        } catch (SQLException ex) {
            Logger.getLogger(frmMatriculas.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

