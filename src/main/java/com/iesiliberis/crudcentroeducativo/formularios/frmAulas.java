    private void txtbuscarKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtbuscarKeyPressed
        if (evt.getKeyCode()==KeyEvent.VK_ENTER){
            DefaultTableModel modelo = (DefaultTableModel) jtAulas.getModel();
            TableRowSorter<TableModel> trsorter= new TableRowSorter(modelo);
            jtAulas.setRowSorter(trsorter);
            if (txtbuscar.getText().length()==0){
                trsorter.setRowFilter(null);
            }else{
                trsorter.setRowFilter(RowFilter.regexFilter(txtbuscar.getText().trim()));
            }
            
        }
    }//GEN-LAST:event_txtbuscarKeyPressed

    private void jtAulasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtAulasMouseClicked
        int fila = jtAulas.getSelectedRow();
        txtCodigo.setText(String.valueOf(jtAulas.getValueAt(fila, 1)));
        txtDescripcion.setText(String.valueOf(jtAulas.getValueAt(fila, 2)));
    }//GEN-LAST:event_jtAulasMouseClicked

    private void btnAñadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAñadirActionPerformed
        aulaDaoImp aul=aulaDaoImp.getInstance();
        aula aula= new aula(txtCodigo.getText(),txtDescripcion.getText());
        
        try {
            if (txtCodigo.getText().equals("")){
                JOptionPane.showMessageDialog(this,"Introduzca como minimo un Codigo");
            }
            else {
            aul.add(aula);
            cargatabla();
            txtCodigo.setText("");
            txtDescripcion.setText("");
            }
            
        } catch (SQLException ex) {
            System.out.println("error" + ex.getMessage());
        }
    }//GEN-LAST:event_btnAñadirActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        aulaDaoImp aul=aulaDaoImp.getInstance();
        int fila = jtAulas.getSelectedRow();
        String id=String.valueOf(jtAulas.getValueAt(fila, 0));
        aula aula= new aula(Integer.parseInt(id),txtCodigo.getText(),txtDescripcion.getText());
        
        try {
            aul.update(aula);
            cargatabla();
            txtCodigo.setText("");
            txtDescripcion.setText("");
            
            
        } catch (SQLException ex) {
            System.out.println("error" + ex.getMessage());
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        int fila = jtAulas.getSelectedRow();
        String id = String.valueOf(jtAulas.getValueAt(fila, 0));
        aulaDaoImp aula=aulaDaoImp.getInstance();
        try {
            aula.delete(Integer.parseInt(id));
            cargatabla();
        } catch (SQLException ex) {
            System.out.println("Error" + ex.getMessage());;
        }
    }//GEN-LAST:event_btnBorrarActionPerformed

