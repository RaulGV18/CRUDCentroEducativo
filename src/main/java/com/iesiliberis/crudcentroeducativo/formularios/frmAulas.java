    private void configtabla(){
        String col[]={"ID","CODIGO","DESCRIPCION"};
        jtAulas.getModel();
        DefaultTableModel mode = new DefaultTableModel(col,0){
                @Override
                public boolean isCellEditable(int row,int column){
                    return false;
                }
        };
        jtAulas.setModel(mode);
    }
    private void cargatabla() throws SQLException{
        DefaultTableModel modelo = (DefaultTableModel)jtAulas.getModel();
        modelo.setNumRows(0);
        
        aulaDaoImp aul=aulaDaoImp.getInstance();
        List<aula> aulas =aul.getAll();
        String [] fila = new String [3];
            for (aula au:aulas){
                fila[0]=""+String.valueOf(au.getId());
                fila[1]=""+au.getCodigo();
                fila[2]=""+au.getDescripcion();
                modelo.addRow(fila);
            }
    }

