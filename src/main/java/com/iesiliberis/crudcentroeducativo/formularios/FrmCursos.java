    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        CursoDaoImp curs=CursoDaoImp.getInstance();
        curso cur= new curso(txtyearinicio.getText(),txtyearfin.getText(),txt_descrip.getText(),Integer.parseInt(txtidcurso.getText()));
        
        try {
            if (txtyearinicio.getText().equals("")){
                JOptionPane.showMessageDialog(this,"Introduzca como minimo un codigo de curso");
            }
            else {
            curs.add(cur);
            cargatabla();
            txtyearinicio.setText("");
            txtyearfin.setText("");
            txt_descrip.setText("");
            }
            
        } catch (SQLException ex) {
            System.out.println("error" + ex.getMessage());
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

