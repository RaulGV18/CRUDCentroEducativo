    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        int fila = jtabcursos.getSelectedRow();
        String id=String.valueOf(jtabcursos.getValueAt(fila, 0));
        CursoDaoImp curs=CursoDaoImp.getInstance();
        
        curso cur= new curso(Integer.parseInt(id),txtyearinicio.getText(),txtyearfin.getText(),txt_descrip.getText(),Integer.parseInt(txtidcurso.getText()));
        try {
            curs.update(cur);
            cargatabla();
            txtyearinicio.setText("");
            txtyearfin.setText("");
            txt_descrip.setText("");
        } catch (SQLException ex) {
            System.out.println("error" + ex.getMessage());
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void jtabcursosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtabcursosMouseClicked
        int fila = jtabcursos.getSelectedRow();
        txtyearinicio.setText(String.valueOf(jtabcursos.getValueAt(fila, 1)));
        txtyearfin.setText(String.valueOf(jtabcursos.getValueAt(fila, 2)));
        txt_descrip.setText(String.valueOf(jtabcursos.getValueAt(fila, 3)));
        txtcursoaca.setText(String.valueOf(jtabcursos.getValueAt(fila, 4)));
        if (evt.getClickCount()==2){
            
            int idseleccion=Integer.parseInt(jtabcursos.getValueAt(jtabcursos.getSelectedRow(), 0).toString());
            frmUnidad unidad;
            try {
                unidad = new frmUnidad(idseleccion);
                unidad.setVisible(true);
            } catch (SQLException ex) {
                Logger.getLogger(FrmCursos.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jtabcursosMouseClicked

    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        int fila = jtabcursos.getSelectedRow();
        String id = String.valueOf(jtabcursos.getValueAt(fila, 0));
        CursoDaoImp curs=CursoDaoImp.getInstance();
        try {
            curs.delete(Integer.parseInt(id));
            cargatabla();
        } catch (SQLException ex) {
            System.out.println("Error" + ex.getMessage());;
        }
        
                
    }//GEN-LAST:event_btnBorrarActionPerformed

