        txtape1.setText(String.valueOf(jtalumno.getValueAt(fila, 3)));
        txtfNac.setText(String.valueOf(jtalumno.getValueAt(fila, 5)));
        txtdir.setText(String.valueOf(jtalumno.getValueAt(fila, 8)));
        txtcp.setText(String.valueOf(jtalumno.getValueAt(fila, 9)));
        txtpob.setText(String.valueOf(jtalumno.getValueAt(fila, 10)));
        txtprov.setText(String.valueOf(jtalumno.getValueAt(fila, 11)));
        txttel.setText(String.valueOf(jtalumno.getValueAt(fila, 6)));
        txtemail.setText(String.valueOf(jtalumno.getValueAt(fila, 7)));
        
        if (evt.getClickCount()==2){
            JDialog frame=new JDialog(this,"Detalle Alumno",true);
            jpAlumnoDetalle panel=new jpAlumnoDetalle();
            int idseleccion=Integer.parseInt(jtalumno.getValueAt(jtalumno.getSelectedRow(), 0).toString());
            panel.CargaDetalle(idseleccion);
            frame.getContentPane().add(panel);
            frame.pack();
            frame.setVisible(true);
        }
