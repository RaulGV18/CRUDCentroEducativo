    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        autorizadoDaoImp auto=autorizadoDaoImp.getInstance();
        autorizado aut=null;
        if (cbParentesco.getSelectedItem().equals("TUTOR1") && tut1 ||cbParentesco.getSelectedItem().equals("TUTOR2") && tut2){
            JOptionPane.showMessageDialog(this,"EL TUTOR INTRODUCIDO YA EXISTE");
            try {
                cargatabla();
            } catch (SQLException ex) {
                Logger.getLogger(frmAutorizados.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else{
            if (cbParentesco.getSelectedItem().equals("TUTOR1")){
            aut= new autorizado(txtdni.getText(),txtnombre.getText(),txtapellido1.getText(),txtapellido2.getText(),parentesco.TUTOR1);
        }
        if (cbParentesco.getSelectedItem().equals("TUTOR2")){
           aut= new autorizado(txtdni.getText(),txtnombre.getText(),txtapellido1.getText(),txtapellido2.getText(),parentesco.TUTOR2);
        }
        if (cbParentesco.getSelectedItem().equals("OTROS")){
            aut = new autorizado(txtdni.getText(),txtnombre.getText(),txtapellido1.getText(),txtapellido2.getText(),parentesco.OTROS);
        }
        
        try {
            if (txtdni.getText().equals("")){
                JOptionPane.showMessageDialog(this,"Introduzca como minimo un DNI");
            }
            else {
            auto.add(aut);
            cargatabla();
            txtdni.setText("");
            txtnombre.setText("");
            txtapellido1.setText("");
            txtapellido2.setText("");
            }
            
        } catch (SQLException ex) {
            System.out.println("error" + ex.getMessage());
        }
        autorizado autor=auto.devolvAutorizadodni(aut.getDni());
        auto.addautorizado(idAlumno, autor.getId());
            try {
                cargatabla();
            } catch (SQLException ex) {
                Logger.getLogger(frmAutorizados.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jtAutorizadosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtAutorizadosMouseClicked
        int fila = jtAutorizados.getSelectedRow();
        txtdni.setText(String.valueOf(jtAutorizados.getValueAt(fila, 1)));
        txtnombre.setText(String.valueOf(jtAutorizados.getValueAt(fila, 2)));
        txtapellido1.setText(String.valueOf(jtAutorizados.getValueAt(fila, 3)));
        txtapellido2.setText(String.valueOf(jtAutorizados.getValueAt(fila, 4)));
        if (jtAutorizados.getValueAt(fila, 5).equals("TUTOR1")){
            cbParentesco.setSelectedItem("TUTOR1");
        }
        if (jtAutorizados.getValueAt(fila, 5).equals("TUTOR2")){
           cbParentesco.setSelectedItem("TUTOR2");
        }
        if (jtAutorizados.getValueAt(fila, 5).equals("OTROS")){
            cbParentesco.setSelectedItem("OTROS");

        }
    }//GEN-LAST:event_jtAutorizadosMouseClicked

