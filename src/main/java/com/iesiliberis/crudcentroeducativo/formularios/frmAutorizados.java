      private void configtabla(){
        String col[]={"ID","DNI","NOMBRE","APELLIDO1","APELLIDO2","PARENTESCO"};
        jtAutorizados.getModel();
        DefaultTableModel mode = new DefaultTableModel(col,0){
                @Override
                public boolean isCellEditable(int row,int column){
                    return false;
                }
        };
        jtAutorizados.setModel(mode);
      }
        private void cargatabla() throws SQLException{
              tut1=false;
              tut2=false;
        DefaultTableModel modelo = (DefaultTableModel)jtAutorizados.getModel();
        modelo.setNumRows(0);
        
        alumnoDaoImp aut=alumnoDaoImp.getInstance();
        List<autorizado> auto =aut.getautorizados(idAlumno);
        String [] fila = new String [6];
            for (autorizado au:auto){
                fila[0]=""+String.valueOf(au.getId());
                fila[1]=""+au.getDni();
                fila[2]=""+au.getNombre();
                fila[3]=""+au.getApellido1();
                fila[4]=""+au.getApellido2();
                if (String.valueOf(au.getParent()).equals("TUTOR1")){
                    tut1=true;
                }
                if (String.valueOf(au.getParent()).equals("TUTOR2")){
                    tut2=true;
                }
                
                fila[5]=""+au.getParent();
                modelo.addRow(fila);
            }
    }

