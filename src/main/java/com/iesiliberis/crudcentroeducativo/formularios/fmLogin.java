    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed

        usuarioDaoImp veri=usuarioDaoImp.getInstance();
        try {
            System.out.println(veri.valida(txtUsuario.getText(), String.valueOf(txtPassword.getPassword())));
        } catch (SQLException ex) {
            System.out.println("error" + ex.getMessage());
        }
        try {
            if (veri.valida(txtUsuario.getText(), String.valueOf(txtPassword.getPassword()))){
                JOptionPane.showMessageDialog(this,"usuario correcto");
                frmMain fmain= new frmMain();
                fmain.setVisible(true);
                this.dispose();
            }
            else {
                JOptionPane.showMessageDialog(this,"usuario incorrecto");
            }
        } catch (SQLException ex) {
            System.out.println("error" + ex.getMessage());
        }
        
    }//GEN-LAST:event_btnAceptarActionPerformed

