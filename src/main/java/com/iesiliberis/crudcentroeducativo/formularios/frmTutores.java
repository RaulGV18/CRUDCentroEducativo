    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        personalDaoImp per=personalDaoImp.getInstance();
        personal pers= new personal(txtdni.getText(),txtnombre.getText(),txtape1.getText(),txtape2.getText(),txtdireccion.getText(),txtcp.getText(),txtpob.getText(),txtprov.getText(),txttele.getText(),txtemail.getText(),1);
        
        try {
            if (txtdni.getText().equals("")){
                JOptionPane.showMessageDialog(this,"Introduzca como minimo un DNI");
            }
            else {
            per.add(pers);
            cargatabla();
            txtdni.setText("");
            txtnombre.setText("");
            txtape1.setText("");
            txtape2.setText("");
            txtdireccion.setText("");
            txtcp.setText("");
            txtpob.setText("");
            txtprov.setText("");
            txttele.setText("");
            txtemail.setText("");
            }
            
        } catch (SQLException ex) {
            System.out.println("error" + ex.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        personalDaoImp per=personalDaoImp.getInstance();
        int fila = jtTutores.getSelectedRow();
        String id=String.valueOf(jtTutores.getValueAt(fila, 0));
        personal pers= new personal(Integer.parseInt(id),txtdni.getText(),txtnombre.getText(),txtape1.getText(),txtape2.getText(),txtdireccion.getText(),txtcp.getText(),txtpob.getText(),txtprov.getText(),txttele.getText(),txtemail.getText(),1);
        
        try {
            per.update(pers);
            cargatabla();
            txtdni.setText("");
            txtnombre.setText("");
            txtape1.setText("");
            txtape2.setText("");
            txtdireccion.setText("");
            txtcp.setText("");
            txtpob.setText("");
            txtprov.setText("");
            txttele.setText("");
            txtemail.setText("");
            
            
        } catch (SQLException ex) {
            System.out.println("error" + ex.getMessage());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int fila = jtTutores.getSelectedRow();
        String id = String.valueOf(jtTutores.getValueAt(fila, 0));
        personalDaoImp pers=personalDaoImp.getInstance();
        try {
            pers.delete(Integer.parseInt(id));
            cargatabla();
        } catch (SQLException ex) {
            System.out.println("Error" + ex.getMessage());;
        }
    }//GEN-LAST:event_jButton3ActionPerformed

