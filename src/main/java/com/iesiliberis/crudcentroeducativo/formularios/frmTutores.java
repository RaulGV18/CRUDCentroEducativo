        private void configtabla(){
        String col[]={"ID","DNI","NOMBRE","APELLIDO1","APELLIDO2","DIRECCION","CP","POBLACION","PROVINCIA","TELEFONO","EMAIL"};
        jtTutores.getModel();
        DefaultTableModel mode = new DefaultTableModel(col,0){
                @Override
                public boolean isCellEditable(int row,int column){
                    return false;
                }
        };
        jtTutores.setModel(mode);
    }
    private void cargatabla() throws SQLException{
        DefaultTableModel modelo = (DefaultTableModel)jtTutores.getModel();
        modelo.setNumRows(0);
        
        personalDaoImp pers=personalDaoImp.getInstance();
        List<personal> person =pers.getProfesores();
        String [] fila = new String [11];
            for (personal per:person){
                fila[0]=""+String.valueOf(per.getId());
                fila[1]=""+per.getDni();
                fila[2]=""+per.getNombre();
                fila[3]=""+per.getApellido1();
                fila[4]=""+per.getApellido2();
                fila[5]=""+per.getDireccion();
                fila[6]=""+per.getCp();
                fila[7]=""+per.getPoblacion();
                fila[8]=""+per.getProvincia();
                fila[9]=""+per.getTelefono();
                fila[10]=""+per.getEmail();
                modelo.addRow(fila);
            }
    }

