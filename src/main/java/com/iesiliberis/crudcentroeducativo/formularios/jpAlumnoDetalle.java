    public void CargaDetalle(int id){
        this.id=id;
        lblid.setText("Alumno Numero: "+ id);
        alumnoDaoImp al=alumnoDaoImp.getInstance();
        al.getById(id);
        txtNombre.setText(al.getById(id).getNombre());
        txtapellido1.setText(al.getById(id).getApellido1());
        txtApellido2.setText(al.getById(id).getApellido2());
        txtTelefono.setText(String.valueOf(al.getById(id).getTelefono()));
        txtdni.setText(al.getById(id).getDni());
        txtemail.setText(al.getById(id).getEmail());
        txtdir.setText(al.getById(id).getDireccion());
        txtpob.setText(al.getById(id).getPoblacion());
        txtprov.setText(al.getById(id).getProvincia());
        txtcp.setText(al.getById(id).getCp());
    }
